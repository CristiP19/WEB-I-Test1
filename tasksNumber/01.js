const n = prompt("Introduce a number");

switch (Number(n)) {
  case 0:
    alert('zero');
    break;
  case 2:
    alert('two');
    break;
  case 3:
    alert('three');
    break;
  case 4:
    alert('four');
    break;
  case 5:
    alert('five');
    break;
  case 6:
    alert('six');
    break;
  case 7:
    alert('seven');
    break;
  case 8:
    alert('eight');
    break;
  case 9:
    alert('nine');
    break;
  default:
    alert("chose another number")
}
